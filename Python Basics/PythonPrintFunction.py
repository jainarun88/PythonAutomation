# 1: Return a value
a = 10
# Two objects are passed in print() function
print("a =", a)

b = a
# Three objects are passed in print function
print('a =', a, '= b')

# 2: Using sep and end argument
print("a =", a, sep='dddd', end='\n\n\n')
print("a =", a, sep='0', end='$$$$$')

