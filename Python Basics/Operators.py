# Arithmetic Operators
a = 5
b = 2

print(a + b)  # addition
print(a - b)  # subtraction
print(a * b)  # multiplication
print(a / b)  # division 2.5
print(a // b)  # 2 quotient
print(a % b)  # 1 reminder
print(a ** b)  # square 5**2 = 25

# Relational Operators
# > < >= <= != ==
# Relational operators always returns boolean value True/False

c = 5
d = 10

print(c > d)  # False
print(c < d)  # True
print(c != d)  # True
print(c == d)  # False
print(c >= d)  # False
print(c <= d)  # True

# Logical Operators
# and or not >> work on boolean values and always returns boolean values

e = True
f = False

print(e and f)  # False
print(e or f)  # True
print(not e)  # False
